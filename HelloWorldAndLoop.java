public class HelloWorldAndLoop {
    public static void main(String[] args) {
        // Print Hello, World!
        System.out.println("Hello, World! I Am Sangit Sarkar");

        // Loop to print numbers from 1 to 100
        for (int i = 1; i <= 100; i++) {
            System.out.println(i);
        }
    }
}
